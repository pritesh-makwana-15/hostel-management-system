// Helper Functions
export const getStudentById = (id) => {
  return studentsData.find(student => student.id === id);
};

export const getStudentByEnrollment = (enrollmentNo) => {
  return studentsData.find(student => student.enrollmentNo === enrollmentNo);
};

export const getRoomById = (roomId) => {
  return roomsData.find(room => room.id === roomId);
};

export const getAvailableRooms = (block = null, type = null) => {
  let filtered = roomsData;
  
  if (block) {
    filtered = filtered.filter(room => room.block === block);
  }
  
  if (type) {
    filtered = filtered.filter(room => room.type === type);
  }
  
  return filtered.filter(room => 
    room.beds.some(bed => bed.status === 'Available')
  );
};